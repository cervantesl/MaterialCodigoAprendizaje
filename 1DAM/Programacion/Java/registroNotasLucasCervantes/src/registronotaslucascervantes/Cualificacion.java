public class Cualificacion {
    private int prg;
    private int edd;
    private int bda;
    
    //Contructor
    public Cualificacion(){
       
    }
    
    
    //Metodos
    public int getPrg() {
        return prg;
    }

    public void setPrg(int prg) {
        this.prg = prg;
    }

    public int getEdd() {
        return edd;
    }

    public void setEdd(int edd) {
        this.edd = edd;
    }

    public int getBda() {
        return bda;
    }

    public void setBda(int bda) {
        this.bda = bda;
    }
}
